#!/usr/bin/env node
/**
 * Generates packages/skillerr/README.md (the npm-published README) from
 * the root README.md (the GitHub-displayed one).
 *
 * These used to be two independently hand-edited files. They drifted:
 * the npm copy kept showing stale branding and trust claims for a full
 * release cycle after the root README was fixed, because nothing forced
 * them to stay in sync. This script is the fix — one authored source,
 * one mechanical transform (relative links/images -> absolute GitHub
 * URLs, since the npm copy is viewed from a different location and has
 * no repo checkout backing it), run automatically before every publish
 * via packages/skillerr's `prepack` script. Never hand-edit
 * packages/skillerr/README.md directly; edit README.md and re-run this.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const REPO_BLOB = "https://github.com/dot-skill/skillerr/blob/main/";
const REPO_RAW = "https://raw.githubusercontent.com/dot-skill/skillerr/main/";

const source = readFileSync(join(root, "README.md"), "utf8");

function toAbsolute(path) {
  const clean = path.replace(/^\.\//, "");
  if (clean.startsWith("assets/")) return REPO_RAW + clean;
  return REPO_BLOB + clean;
}

// Matches markdown link targets `](./x)` and image src attributes
// `src="./x"` that point at a repo-relative path — leaves absolute
// URLs (http/https) and in-page anchors (#...) untouched.
const rewritten = source
  .replace(/\]\((\.\/[^)#]+)(#[^)]*)?\)/g, (_m, path, anchor) => `](${toAbsolute(path)}${anchor ?? ""})`)
  .replace(/src="(\.\/[^"]+)"/g, (_m, path) => `src="${toAbsolute(path)}"`);

const banner =
  "<!-- Generated by scripts/sync-npm-readme.mjs from README.md. Do not hand-edit. -->\n\n";

writeFileSync(join(root, "packages/skillerr/README.md"), banner + rewritten);
console.log("packages/skillerr/README.md synced from README.md");
